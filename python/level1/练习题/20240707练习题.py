# 2、条件判断：根据用户的年龄判断其属于哪个年龄段，并打印出相应的信息。
# 如果年龄小于13岁，打印 "你是儿童"
# 如果年龄在13到19岁之间（包含13岁和19岁），打印 "你是青少年"
# 如果年龄大于等于20岁且小于等于59岁，打印 "你是成年人"
# 如果年龄大于59岁，打印 "你是老年人"
age = input('请输入年龄:')
age = int(age)
if age < 13:
    print('你是儿童')
elif age <= 19:
    print('你是青少年')
elif age <= 59:
    print('你是成年人')
elif age > 59:
    print('你是老年人')
# 3、列表与循环：
# 3.1、计算1+2+3+4+...+99的和，并打印结果
s = 0
for i in range(100):
    s += i
print('和: ' + str(s))
# 3.2、计算1x2x3x4x...x99的积，并打印结果
s = 1
