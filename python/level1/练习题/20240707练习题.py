import math
# 1、利用Python
# 1.1、计算指定边长的正方形面积；边长为15；
print(15 * 15)
# 1.2、计算指定长和宽的长方形面积；长为15，宽为8；
print(15 * 8)
# 1.3、计算半径的圆的面积；半径为15；
